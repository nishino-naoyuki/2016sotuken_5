package jp.ac.asojuku.food.dto;

public class HistoryDto {
	
	private String hs_add;
	private String re_name;
	private String re_place;
	/**
	 * hs_addを取得します。
	 * @return hs_add
	 */
	public String getHs_add() {
	    return hs_add;
	}
	/**
	 * hs_addを設定します。
	 * @param hs_add hs_add
	 */
	public void setHs_add(String hs_add) {
	    this.hs_add = hs_add;
	}
	/**
	 * re_nameを取得します。
	 * @return re_name
	 */
	public String getRe_name() {
	    return re_name;
	}
	/**
	 * re_nameを設定します。
	 * @param re_name re_name
	 */
	public void setRe_name(String re_name) {
	    this.re_name = re_name;
	}
	/**
	 * re_placeを取得します。
	 * @return re_place
	 */
	public String getRe_place() {
	    return re_place;
	}
	/**
	 * re_placeを設定します。
	 * @param re_place re_place
	 */
	public void setRe_place(String re_place) {
	    this.re_place = re_place;
	}

}
